import mongoose from "mongoose";

const walletSchema = new mongoose.Schema(
  {
    user: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
      unique: true
    },

    balanceMinutes: {
      type: Number,
      default: 0
    }
  },
  { timestamps: true }
);

export default mongoose.model("Wallet", walletSchema);
